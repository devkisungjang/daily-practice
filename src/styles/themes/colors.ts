export const setColorsToCSSVariables = () => {
  Object.entries(colors).forEach(([colorGroup, shades]) => {
    Object.entries(shades).forEach(([shade, value]) => {
      // 변수로 사용 할 수 있도록 set
      document.documentElement.style.setProperty(
        `--color-${colorGroup}-${shade}`,
        value
      );
    });
  });
};
